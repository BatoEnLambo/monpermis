<p>Pour un projet d'extension simple sur terrain plat, c'est aussi faisable — à condition d'y consacrer le temps nécessaire et de maîtriser au moins un logiciel de dessin. Si vous êtes bricoleur, patient, et que vous avez 3 à 4 week-ends devant vous, le DIY peut fonctionner. Mais soyez lucide sur votre niveau : si vous n'avez jamais ouvert un logiciel de CAO, le temps d'apprentissage à lui seul peut rendre la délégation plus économique.</p>

