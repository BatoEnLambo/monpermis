<p>La RE2020 évalue votre bâtiment sur 6 indicateurs. Vous n'avez pas besoin de comprendre les formules de calcul — c'est le travail du thermicien. Mais savoir ce que chacun mesure vous aide à comprendre les choix de conception de votre maison.</p>

