<p>Le dossier PC utilise le formulaire CERFA 13406*14 et les pièces PCMI1 à PCMI8, qui sont l'équivalent des pièces DP avec un niveau de détail supérieur. S'y ajoutent les attestations obligatoires en 2026 : attestation de conformité RE2020 (qui a remplacé la RT2012), attestation parasismique (PCMI14-1) si vous êtes en zone sismique, et attestation relative au risque argile selon la nature de votre sol.</p>

